package com.example.demo.model;

import java.time.LocalDateTime;

public class ConcreteBooking extends Booking {

    public ConcreteBooking(String id, User user, Car car,
                           LocalDateTime from, LocalDateTime to,
                           double totalPrice, String status) {
        setId(id);
        setUser(user);
        setCar(car);
        setFrom(from);
        setTo(to);
        setTotalPrice(totalPrice);
        setStatus(status);
    }
}
